"""zip() 函数	python内建函数
    将可迭代的对象作为参数，将可迭代对象中的元素打包成一个个元组tuple，然
	后由这些元组组成一个列表list。如果传入的参数长度不等，则返回list的长度和
	参数中最短的对象相同，利用*号操作符，可以将list unzip解压"""

n1 = [1,2,3]
s1 = ["a","b","c","d"]

zip1 = zip(n1)
for i in zip1:
    print(i)    #(1,) (2,) (3,)

zip2 = zip(n1,s1)
for i in zip2:
    print(i)    #(1, 'a') (2, 'b') (3, 'c')  s1中"e"就丢失了,无法组成新的2个元素元组

zip(*zip2)  # [(1,2,3),("a","b","c","d")]
